# Aquí hay una variable de tipo entero
número = 147
print(número) #Esta línea sirve para imprimir la variable número
print(type (número))

# Redefinimos la variable número
número = 147.5
print(número)
print(type (número))

complejo = 1 + 2j
print(complejo)
print(type(complejo))

